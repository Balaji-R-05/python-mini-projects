def add(n1:float, n2:float) -> float:
    return n1+n2

def sub(n1:float, n2:float) -> float:
    return n1-n2

def multiply(n1:float, n2:float) -> float:
    return n1*n2

def divide(n1:float, n2:float) -> float:
    return n1/n2

def mod(n1:float, n2:float) -> float:
    return n1%n2

def exponention(n1:float, n2:float) -> float:
    return n1**n2